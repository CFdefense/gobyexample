package main

import "fmt"

// recursive function executes until it calls itself(0)
func fact(n int) int {
	if n == 0 {
		return 1
	}
	return n * fact(n-1)
}

func main() {
	fmt.Println(fact(7))

	// can also define anonymous functions to do recursion
	var fib func(n int) int

	// initialize the function and call it
	fib = func(n int) int {
		if n < 2 {
			return n
		}

		return fib(n-1) + fib(n-2)
	}

	fmt.Println(fib(7))
}
